-- ============ RPC Functions ============

CREATE OR REPLACE FUNCTION get_due_ml_coding_reviews(p_user_id UUID, p_limit INTEGER DEFAULT 10)
RETURNS SETOF ml_coding_review_queue
LANGUAGE sql STABLE
AS $$
  SELECT *
  FROM ml_coding_review_queue
  WHERE user_id = p_user_id
    AND next_review <= NOW()
  ORDER BY priority DESC, next_review ASC
  LIMIT p_limit;
$$;

CREATE OR REPLACE FUNCTION complete_ml_coding_review(p_review_id UUID, p_success BOOLEAN)
RETURNS void
LANGUAGE plpgsql
AS $$
BEGIN
  IF p_success THEN
    UPDATE ml_coding_review_queue
    SET interval_days = LEAST(interval_days * 2, 30),
        next_review = NOW() + (LEAST(interval_days * 2, 30) || ' days')::INTERVAL,
        review_count = review_count + 1,
        last_reviewed = NOW()
    WHERE id = p_review_id;
  ELSE
    UPDATE ml_coding_review_queue
    SET interval_days = 1,
        next_review = NOW() + INTERVAL '1 day',
        review_count = review_count + 1,
        last_reviewed = NOW()
    WHERE id = p_review_id;
  END IF;
END;
$$;

