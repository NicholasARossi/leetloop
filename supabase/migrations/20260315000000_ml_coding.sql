-- ML Coding Drills
-- Static problem bank, daily exercise batches, and spaced repetition review queue
-- for Amazon ML coding interview prep

-- ============ Problems Table (static bank of 10) ============

CREATE TABLE IF NOT EXISTS ml_coding_problems (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  slug TEXT UNIQUE NOT NULL,
  title TEXT NOT NULL,
  description TEXT NOT NULL,
  difficulty TEXT NOT NULL CHECK (difficulty IN ('easy', 'medium', 'hard')),
  category TEXT NOT NULL,
  key_concepts TEXT[] DEFAULT '{}',
  math_concepts TEXT[] DEFAULT '{}',
  estimated_minutes INTEGER NOT NULL DEFAULT 30,
  sort_order INTEGER NOT NULL DEFAULT 0,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- ============ Daily Exercises Table ============

CREATE TABLE IF NOT EXISTS ml_coding_daily_exercises (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id UUID NOT NULL,
  problem_id UUID REFERENCES ml_coding_problems(id),
  generated_date DATE NOT NULL DEFAULT CURRENT_DATE,
  sort_order INTEGER NOT NULL DEFAULT 0,

  -- Exercise content (generated by Gemini as a specific variation)
  prompt_text TEXT NOT NULL,
  starter_code TEXT,

  -- User response & grading
  submitted_code TEXT,
  score NUMERIC(4,2) CHECK (score IS NULL OR (score >= 0 AND score <= 10)),
  verdict TEXT CHECK (verdict IS NULL OR verdict IN ('pass', 'fail', 'borderline')),
  feedback TEXT,
  correctness_score NUMERIC(4,2) CHECK (correctness_score IS NULL OR (correctness_score >= 0 AND correctness_score <= 10)),
  code_quality_score NUMERIC(4,2) CHECK (code_quality_score IS NULL OR (code_quality_score >= 0 AND code_quality_score <= 10)),
  math_understanding_score NUMERIC(4,2) CHECK (math_understanding_score IS NULL OR (math_understanding_score >= 0 AND math_understanding_score <= 10)),
  missed_concepts TEXT[] DEFAULT '{}',
  suggested_improvements TEXT[] DEFAULT '{}',

  -- Status
  status TEXT NOT NULL DEFAULT 'pending' CHECK (status IN ('pending', 'completed', 'skipped')),
  is_review BOOLEAN DEFAULT FALSE,

  -- Timestamps
  created_at TIMESTAMPTZ DEFAULT NOW(),
  completed_at TIMESTAMPTZ,

  -- Unique constraint: one exercise per sort position per user per day
  UNIQUE(user_id, generated_date, sort_order)
);

-- ============ Review Queue Table ============

CREATE TABLE IF NOT EXISTS ml_coding_review_queue (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id UUID NOT NULL,
  problem_slug TEXT NOT NULL,
  reason TEXT,
  priority INTEGER DEFAULT 0,
  next_review TIMESTAMPTZ DEFAULT NOW(),
  interval_days INTEGER DEFAULT 1,
  review_count INTEGER DEFAULT 0,
  last_reviewed TIMESTAMPTZ,
  created_at TIMESTAMPTZ DEFAULT NOW(),

  UNIQUE(user_id, problem_slug)
);

-- ============ Indexes ============

CREATE INDEX IF NOT EXISTS idx_ml_coding_daily_exercises_user_date
  ON ml_coding_daily_exercises(user_id, generated_date);

CREATE INDEX IF NOT EXISTS idx_ml_coding_daily_exercises_user_status_date
  ON ml_coding_daily_exercises(user_id, status, generated_date);

CREATE INDEX IF NOT EXISTS idx_ml_coding_review_queue_user_next
  ON ml_coding_review_queue(user_id, next_review);

